package interviewPrep;

public class Task14_EvenOrOdd {

    public static void main(String[] args) {

        System.out.println(evenOrOdd(4));
    }

    public static String evenOrOdd(int a){

        return (a % 2 == 0) ? "even" : "odd";
    }
}

